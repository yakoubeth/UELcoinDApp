const contractAddress = "0x797C471688703f69E0Feb30805aeCF3C7633786B";
const provider = window.ethereum;
const web3 = new Web3(provider);
const contract = new web3.eth.Contract(contractABI, contractAddress);

const connectButton = document.getElementById('connect');
const claimButton = document.getElementById('claim');
const transferForm = document.getElementById('transferForm');
const balanceElement = document.getElementById('balance');

let userAccount;

connectButton.addEventListener('click', connectWallet);
claimButton.addEventListener('click', claimTokens);
transferForm.addEventListener('submit', transferTokens);

async function connectWallet() {
    if (typeof window.ethereum !== 'undefined') {
        try {
            const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
            userAccount = accounts[0];
            updateBalance();
        } catch (error) {
            console.error('User denied account access', error);
        }
    } else {
        alert('Please install MetaMask or another compatible wallet.');
    }
}

async function updateBalance() {
    if (userAccount) {
        const balance = await contract.methods.balanceOf(userAccount).call();
        balanceElement.innerHTML = `UEL ${web3.utils.fromWei(balance, 'ether')} <span class="currency">$</span>`;
    }
}



async function claimTokens() {
    if (userAccount) {
        await contract.methods.claim().send({ from: userAccount });
        updateBalance();
    } else {
        alert('Please connect your wallet first.');
    }
}

async function transferTokens(event) {
    event.preventDefault();
    if (userAccount) {
        const recipient = document.getElementById('recipient').value;
        const amount = document.getElementById('amount').value;
        const weiAmount = web3.utils.toWei(amount, 'ether');

        await contract.methods.transfer(recipient, weiAmount).send({ from: userAccount });
        updateBalance();
    } else {
        alert('Please connect your wallet first.');
    }
}
